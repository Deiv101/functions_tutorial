# Creating a function
def my_function():
    print("Hello from a function")

# Calling a Function
my_function()

# Arguments
def my_function(fname):
    print(fname + " Rakojoana")
my_function("David")
my_function("Lilly")
my_function("Amo")

# Number of Parameters
def my_function(fname, lname):
    print(fname + " " + lname)

my_function("Deiv", "Rakojoana")
